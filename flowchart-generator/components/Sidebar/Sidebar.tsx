interface SidebarProps {
  currentPath?: string;
}

export default function Sidebar({ currentPath = '' }: SidebarProps) {
  const [collapsed, setCollapsed] = useState(false);
  const { theme, toggleTheme } = useTheme();
  const { data: session } = useSession();

  return (
    <div className={`${collapsed ? 'w-16' : 'w-64'} h-screen bg-light-darker dark:bg-dark-lighter fixed left-0 top-0 transition-all duration-300 z-20 flex flex-col`}>
      <div className="flex items-center justify-between p-4 border-b border-gray-200 dark:border-gray-700">
        {!collapsed && (
          <h1 className="text-lg font-bold text-dark dark:text-white">Flow Chart</h1>
        )}
        <button 
          onClick={() => setCollapsed(!collapsed)}
          className="p-1 rounded-md hover:bg-gray-200 dark:hover:bg-gray-700"
        >
          <ChevronRightIcon />
        </button>
      </div>

      <nav className="flex-1 overflow-y-auto py-4">
        <ul className="space-y-2 px-2">
          <li>
            <Link href="/" 
              className={`sidebar-item ${currentPath === '/' ? 'active' : ''}`}
            >
              <HomeIcon />
              {!collapsed && <span>Home</span>}
            </Link>
          </li>
          <li>
            <Link href="/create" 
              className={`sidebar-item ${currentPath === '/create' ? 'active' : ''}`}
            >
              <PlusIcon />
              {!collapsed && <span>New Flow Chart</span>}
            </Link>
          </li>
          {session && (
            <li>
              <Link href="/dashboard" 
                className={`sidebar-item ${currentPath === '/dashboard' ? 'active' : ''}`}
              >
                <DashboardIcon />
                {!collapsed && <span>My Flow Charts</span>}
              </Link>
            </li>
          )}
          <li>
            <Link href="/settings" 
              className={`sidebar-item ${currentPath === '/settings' ? 'active' : ''}`}
            >
              <SettingsIcon />
              {!collapsed && <span>Settings</span>}
            </Link>
          </li>
        </ul>
      </nav>

      <div className="p-4 border-t border-gray-200 dark:border-gray-700 space-y-2">
        <button 
          onClick={toggleTheme}
          className="sidebar-item w-full justify-start"
        >
          {theme === 'dark' ? <SunIcon /> : <MoonIcon />}
          {!collapsed && <span>{theme === 'dark' ? 'Light Mode' : 'Dark Mode'}</span>}
        </button>
        
        {session ? (
          <button 
            onClick={() => signOut()}
            className="sidebar-item w-full justify-start"
          >
            <LogoutIcon />
            {!collapsed && <span>Logout</span>}
          </button>
        ) : (
          <Link href="/login" 
            className={`sidebar-item ${currentPath === '/login' ? 'active' : ''}`}
          >
            <LoginIcon />
            {!collapsed && <span>Login</span>}
          </Link>
        )}
      </div>
    </div>
  );
}